export class Ticket {
	timeline: Timeline;
	shape: Shape;
	shape2: Shape;
	el: HTMLElement;
	el2: HTMLElement;

	constructor(el, el2) {
		this.initShapes();
		this.timeline = new Timeline();
		this.el = el;
		this.el2 = el2;
		this.createShape();
	}

	public open() {
		let vEl: any = this.el.querySelector('.ticketVertical');

		let leftHeaderItems = document.querySelector('c-header-left');
		let RightHeaderItems = document.querySelector('c-header-right');
		let chanelier = document.querySelector('c-chandelier');

		this.moveItemHorizontal(leftHeaderItems, -window.innerWidth / 4);
		this.moveItemHorizontal(RightHeaderItems, window.innerWidth / 4);
		this.moveItemVertical(chanelier, -window.innerHeight);

		this.shape.pause();
		vEl.style.transform = `translateY(600px) rotate(90deg)`;
		this.createHorizontalShape();
	}

	initShapes() {
		addShape('TicketShape', TicketShape);
	}


	moveItemHorizontal(el, to) {
		let tween = new Tween({
			delay: 0,
			duration: 1500,
			isYoyo: true,
			repeat: 1,
			easing: easing.cubic.inout,
			onUpdate: (ep, p) => {
				let v = ep * to;
				el.style.transform = `translateX(${v}px)`;
			}
		})
		tween.play();
	}

	moveItemVertical(el, to) {
		let tween = new Tween({
			delay: 750,
			duration: 3000,
			easing: easing.cubic.out,
			onUpdate: (ep, p) => {
				let v = ep * to;
				el.style.transform = `translateY(${v}px)`;
			}
		})
		tween.play();
	}

	createShape() {
		let width = 1000;
		let height = width * 0.51;
		this.shape = new Shape({
			shape: 'TicketShape',
			className: 'ticketVertical',
			isShowEnd: true,
			isShowStart: true,
			parent: this.el,
			easing: easing.elastic.inout,
			top: window.innerHeight,
			scale: 1,
			origin: '50% 50%',
			angle: 90,
			delay: 10250,
			x: 0,
			y: { '100%': '25%' },
			duration: 1800,
			width: width,
			height: height,
		}).then({
			delay: 5000,
			isYoyo: true,
			duration: 800,
			easing: easing.cubic.inout,
			repeat: 99,
			y: { '25%': '20%' },
		});
		this.shape.play();
	}

	createHorizontalShape() {
		let width = 626;
		let height = width * 0.51;
		this.shape2 = new Shape({
			shape: 'TicketShape',
			className: 'ticketHorizontal',
			isShowEnd: true,
			isShowStart: false,
			delay: 1500,
			parent: this.el2,
			easing: easing.elastic.inout,
			top: window.innerHeight / 2,
			scale: 0.95,
			origin: '50% 50%',
			angle: 90,
			x: 0,
			y: { '250%': '15%' },
			duration: 3000,
			width: width,
			height: height,
		});
		this.shape2.play();
	}
}



